/* Event List */
CREATE TABLE EL (
	CODE		NUMERIC(5),
	TEXT		CHAR(100),
	TEXT_Q0		CHAR(100),
	TEXT_Q1		CHAR(100),
	TEXT_QN		CHAR(100),
	N		CHAR(1),
	Q		NUMERIC(2),
	T		CHAR(3),
	D		NUMERIC(1),
	U		CHAR(1),
	C		NUMERIC(2),
	R		CHAR(20),
	COMMENT		CHAR(100),	/* to be removed */
	PRIMARY KEY (CODE)
);

/* Forecase Event List */
CREATE TABLE FEL (
	CODE		NUMERIC(5),
	TEXT		CHAR(100),
	TEXT_Q0		CHAR(100),
	TEXT_Q1		CHAR(100),
	TEXT_QN		CHAR(100),
	N		CHAR(1),
