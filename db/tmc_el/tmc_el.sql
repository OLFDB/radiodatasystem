/* Phrase Codes */
CREATE TABLE PC (
	L		CHAR(1),
	N		NUMERIC(5),
	TEXT		CHAR(100),
	TEXT_Q0		CHAR(100),
	TEXT_Q1		CHAR(100),
	TEXT_QN		CHAR(100),
	COMMENT		CHAR(100),	/* to be removed */
	PRIMARY KEY (L, N)
);

